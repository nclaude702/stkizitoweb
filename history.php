This section includes the historical background of the school and disaggregated data about staff and students.

  <p>Historical background of the school</p>

 (Foundation dates, ownership, location and rationale for foundation, overview of combination offered at the school, student and staff population, etc.)

Saint Kizito Save TVET school is located at Save. It is a few miles north of the town Huye, and in the west of the Gisagara District of Southern Province, Rwanda. It is a place characterized by dwellings, several schools, church, health center and other facilities operated by a religious group for the purpose of providing charitable services and to propagate religion.
Before 1962, Saint Kizito Save TVET School was an orphanage for children mulattoes of Save, running by white sisters. From 1962 to 1972, the institution became a junior seminary for catholic fathers to be. In 1972, the former bishop of Butare, his Lordship Jean Baptiste GAHAMANYI decided to shift the seminary to Butare (karubanda).  At the same period, the bishop invited Van Dale Brothers to open the center for technicians. According to the official letter wrote by the Ministry of education on 22nd of December, 1972, the center opened and received the first students on 2nd of January, 1973 at Save. 
<ul><li><i class="bi bi-check-circle"></i>From 1973 to 1981, the name of the school was Centre de Formation Technique (CFT) or Technical Training Center, with two sections: Electricity and Masonry.</li>    
<li> <i class="bi bi-check-circle"></i>Between 1981 and 2000, the school started to train technicians A3 level and changed its name by Ecole Technique Inférieure de Save (ETI Save), with four sections: Electricity, Carpentry, Masonry, Plumbing and Welding. </li>
<li><i class="bi bi-check-circle"></i>Since 2000, after agreement with the bishop of the Butare diocese of, his lordship Philippe RUKAMBA, the Congregation of Brothers of Saint Gabriel started running the school with students of A3 level in Construction, Electricity, Plumbing and Welding. </li>
<li><i class="bi bi-check-circle"></i> In 2001-2002, the government decided to eliminate the A3 level, and the school began to train students of A2 level in three sections: Electricity, Public works and Computer Electronics</li> 
<li><i class="bi bi-check-circle"></i>From 2008, the school supplemented two other sections (computer science and construction) in addition to those already there. </li>
<li> <i class="bi bi-check-circle"></i>In 2015, the school added carpentry as a new section.</li> 
<li> <i class="bi bi-check-circle"></i>In 2017, the school added Software development, Surveying trades when WDA started in TVET schools trades in replacement of sections.</li> 
<li> <i class="bi bi-check-circle"></i>In 2018, the school added also Road construction, Accounting, Business services and ICT Multimedia as new trades.</li></ul>  
Saint Kizito Save TVET is a legally Established Government Aided Institution; it is under the authority of WDA, since the GoR decided to establish Workforce Development Authority (WDA), formally approved by the Cabinet on January 18, 2008. Therefore, IPRC-S acts as WDA base for implementation of technical and vocational education and training, programs, supervision and coordination of all TVET institutions in the Province. From 2018, RP (Rwanda Polytechnic) has a mandate to help TVET schools academically including our school; Saint Kizito Save TVET is one of the TVET Institutions in the Gisagara District of Southern Province, Rwanda. RP supervises, implements and coordinates school activities academically. 
According to the Presidential Order N° 123/01 of 15/10/2020 establishing Rwanda TVET Board (RTB) as published in Official gazette n° 32 bis of 19/10/2020; with a mandate of development and coordinating all TVET schools, now Saint Kizito Save TVET School like other TVET institutions is working under GISAGARA District and respectively Rwanda TVET Board (RTB).
